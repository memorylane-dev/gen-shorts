#!/bin/bash
# ============================================================
# (부록) 영상 특정 구간 자르기 (재인코딩 없이 빠른 복사)
# ============================================================

set -e

INPUT="${1:?사용법: $0 <입력파일> <시작시간> <길이> <출력파일>}"
START="${2:?시작시간 필요 (예: 04:50)}"
DURATION="${3:?길이 필요 (예: 00:20:00)}"
OUTPUT="${4:?출력파일명 필요}"

echo "=== 영상 자르기 ==="
echo "  입력: $INPUT"
echo "  구간: $START ~ +$DURATION"
echo "  출력: $OUTPUT"

ffmpeg -y -ss "$START" \
  -i "$INPUT" \
  -t "$DURATION" \
  -c copy \
  "$OUTPUT"

echo "=== 완료 ==="
ls -lh "$OUTPUT"
